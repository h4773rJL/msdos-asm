INCLUDE PERSA.BIB
.MODEL SMALL
.STACK
.DATA
	M1 DB 'MAYUSCULAS Y/O MINUSCULAS',0AH,0DH,'$'
.CODE
INI:	MOV	AX,@DATA
	MOV 	DS,AX
	CLS
	WRITE	M1
LEER:	MOV 	AH,01
        INT     21H
	CMP     AL,2Eh
	JZ      FINAL
	CMP     AL,41h
	JL      LEER
	CMP	AL,5AH
	JLE	MINUS
	CMP     AL,61H
	JL      LEER
	CMP     AL,7AH
	JLE     MAYUS
	JMP     LEER
MINUS:	CALL    MAYMIN
	JMP     LEER
MAYUS:	CALL    MINMAY
	JMP     LEER
FINAL:	MOV 	AX,4C00H
	INT 	21H
	
;INICIO DE PROCEDURES
;PROGRAM BY PERSA (THANKS CLAUDIA)

MAYMIN PROC NEAR
        ADD     AL,20H
        MOV     DL,AL
        MOV     AH,02
        INT     21H
        RET
MAYMIN ENDP

MINMAY PROC NEAR
        SUB     AL,20H
        MOV     DL,AL
        MOV     AH,02
        INT     21H
        RET
MINMAY ENDP

END INI
